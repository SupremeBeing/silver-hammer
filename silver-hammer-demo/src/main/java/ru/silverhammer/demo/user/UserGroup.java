package ru.silverhammer.demo.user;

import ru.silverhammer.processor.Caption;
import ru.silverhammer.control.Text;

public class UserGroup {
	
	public enum Type {
		Admin,
		Guest,
		User,
		Operator,
		RemoteAccess
	}

	@Text
	@Caption("Name")
	private String name;
	
	@Text
	@Caption("Description")
	private String description;

	@Text
	@Caption("Type")
	private Type type;
	
	public UserGroup() {}
	
	public UserGroup(String name, String description, Type type) {
		this.name = name;
		this.description = description;
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public String getDescription() {
		return description;
	}

	public Type getType() {
		return type;
	}
}
